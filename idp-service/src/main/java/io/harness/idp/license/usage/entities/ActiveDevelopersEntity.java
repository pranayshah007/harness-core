package io.harness.idp.license.usage.entities;

import io.harness.annotation.HarnessEntity;
import io.harness.annotations.StoreIn;
import io.harness.annotations.dev.HarnessTeam;
import io.harness.annotations.dev.OwnedBy;
import io.harness.mongo.index.CompoundMongoIndex;
import io.harness.mongo.index.MongoIndex;
import io.harness.ng.DbAliases;
import io.harness.persistence.CreatedAtAware;
import io.harness.persistence.PersistentEntity;
import io.harness.persistence.UpdatedAtAware;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.google.common.collect.ImmutableList;
import dev.morphia.annotations.Entity;
import dev.morphia.annotations.Id;
import java.util.List;
import javax.validation.constraints.NotNull;
import lombok.AccessLevel;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import lombok.experimental.FieldDefaults;
import lombok.experimental.FieldNameConstants;
import org.springframework.data.annotation.CreatedDate;
import org.springframework.data.annotation.LastModifiedDate;
import org.springframework.data.mongodb.core.mapping.Document;

@Data
@Builder
@FieldNameConstants(innerTypeName = "ActiveDevelopersKeys")
@FieldDefaults(level = AccessLevel.PRIVATE)
@JsonIgnoreProperties(ignoreUnknown = true)
@NoArgsConstructor
@AllArgsConstructor
@StoreIn(DbAliases.IDP)
@Entity(value = "activeDevelopers", noClassnameStored = true)
@Document("activeDevelopers")
@HarnessEntity(exportable = true)
@OwnedBy(HarnessTeam.IDP)
public class ActiveDevelopersEntity implements PersistentEntity, CreatedAtAware, UpdatedAtAware {
  @JsonIgnore @Id private String id;
  @NotNull String accountIdentifier;
  @NotNull String userIdentifier;
  @NotNull String email;
  @NotNull String userName;
  @NotNull long lastAccessedAt;
  @JsonIgnore @CreatedDate long createdAt;
  @JsonIgnore @LastModifiedDate long lastUpdatedAt;

  public static List<MongoIndex> mongoIndexes() {
    return ImmutableList.<MongoIndex>builder()
        .add(CompoundMongoIndex.builder()
                 .name("unique_accountIdentifier_userIdentifier")
                 .field(ActiveDevelopersKeys.accountIdentifier)
                 .field(ActiveDevelopersKeys.userIdentifier)
                 .unique(true)
                 .build())
        .build();
  }
}
